"use strict";
// Função simples de multiplicação com TypeScript:
function multiplicaType(x, y) {
    return x * y;
}
// Função simples de concatenação com TypeScript:
function concatenaNome(nome) {
    return `Olá ${nome}, seja bem vindo!`;
}
// Testando os retornos das funções:
console.log(multiplicaType(120, 2));
console.log(concatenaNome('Henrique'));
